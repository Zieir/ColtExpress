package jeuColtExpress.Modeles;
/**
 * L'énumération Action représente les actions possibles qu'un personnage peut effectuer dans le jeu.
 * Les actions comprennent : monter dans un wagon, descendre d'un wagon, avancer vers le wagon suivant,
 * reculer vers le wagon précédent, tirer sur un autre personnage, braquer le wagon pour obtenir des butins,
 */
public enum Action {
    Monter,
    Avance,
    Descendre,
    Recule,
    Tirer,
    Braquer
}